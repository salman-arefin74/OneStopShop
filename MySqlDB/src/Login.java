    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        connect = MyDatabaseHandler.ConnectDb();
        String sql = "select * from user where user_type=? and user_id=? and password=?";
        try{
            pst = connect.prepareStatement(sql);
            pst.setString(1, txttype.getText());
            pst.setString(2, txtid.getText());
            pst.setString(3, txtpass.getText());
            rs = pst.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "Welcome User");
                TableClass w = new TableClass(connect);
                w.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Incorrect Username or password", "Access Denied", JOptionPane.ERROR_MESSAGE);
                
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        
    }
        
    }//GEN-LAST:event_btnSubmitActionPerformed

    
